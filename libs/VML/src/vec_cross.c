#include "../VML.h"

t_vec3	vec3_cross_vec3(t_vec3 v, t_vec3 v1)
{
	return ((t_vec3){
		v.y * v1.z - v.z * v1.y,
		v.z * v1.x - v.x * v1.z,
		v.x * v1.y - v.y * v1.x
	});
}
